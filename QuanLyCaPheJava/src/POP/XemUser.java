public class XemUser {
    private String Name, Crea, Ex_day, Account, Last, Prof;

    public String getName() {
        return Name;
    }

    public void setName(String Name1) {
       Name = Name1;
    }

    public String getCrea() {
        return Crea;
    }

    public void setCrea(String Crea1) {
        Crea = Crea1;
    }

    public String getEx_day() {
        return Ex_day;
    }

    public void setEx_day(String Ex_day1) {
        Ex_day = Ex_day1;
    }

    public String getAccount() {
        return Account;
    }

    public void setAccount(String Account1) {
        Account = Account1;
    }

    public String getLast() {
        return Last;
    }

    public void setLast(String Last1) {
        Last = Last1;
    }

    public String getProf() {
        return Prof;
    }

    public void setProf(String Prof1) {
        Prof = Prof1;
    }
}
