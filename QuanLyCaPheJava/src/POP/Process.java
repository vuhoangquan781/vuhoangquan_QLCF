public class Process {
    private String PID;
    private String SOSID;
    private String SPID;

    public String getPID() {
        return PID;
    }

    public void setPID(String PID1) {
        PID = PID1;
    }

    public String getSOSID() {
        return SOSID;
    }

    public void setSOSID(String SOSID1) {
        SOSID = SOSID1;
    }

    public String getSPID() {
        return SPID;
    }

    public void setSPID(String SPID1) {
       SPID = SPID1;
    }
}
